I=imread('Moon.bmp');
figure;
subplot(1,2,1)
imshow(uint8(I))
H=rgb2gray(I); 
subplot(1,2,2)
imhist(H)
F=rgb2hsv(I);
F(:,:,2)=F(:,:,2)*1.8;
F(:,:,3)=F(:,:,3)*5/3-1/3;
Iout=hsv2rgb(F)*255;
H2=rgb2gray(uint8(Iout));
figure;
subplot(1,2,2)
imhist(H2);
subplot(1,2,1)
imshow(uint8(Iout));
imwrite(uint8(Iout),'peppers2.bmp');